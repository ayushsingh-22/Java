package tools;

public class advcalc extends calc
{
    public int power(int a, int b)
    {
        return (int)Math.pow(a, b);
    }

    public int min(int a, int b)
    {
        return (int)Math.min(a, b);
    }

    public int max(int a, int b)
    {
        return (int)Math.max(a, b);
    }
    
}
