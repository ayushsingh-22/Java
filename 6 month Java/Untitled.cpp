#include <iostream>
using namespace std;

int main() {
    // Variables to store the numbers
    int num1, num2, sum;

    // Input from user
    cout << "Enter the first number: ";
    cin >> num1;

    cout << "Enter the second number: ";
    cin >> num2;

    // Add the numbers
    sum = num1 + num2;

    // Display the result
    cout << "The sum of " << num1 << " and " << num2 << " is: " << sum << endl;

    return 0;
}
